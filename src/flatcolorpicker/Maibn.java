    public String gethex(javax.swing.JLabel g){
        return ("#" + String.format("%06x", g.getBackground().getRGB() & (0x00FFFFFF)));
    }
    public static String hex2Rgb(String colorStr) {
        return (Integer.valueOf( colorStr.substring( 1, 3 ), 16 )+ ","+Integer.valueOf( colorStr.substring( 3, 5 ), 16 )+ ","+Integer.valueOf( colorStr.substring( 5, 7 ), 16 ) + "") ;
    }
