public void color(String r,String h){
    
    Clipboard clpbrd = Toolkit.getDefaultToolkit().getSystemClipboard ();        
    if(hexrgb == false) clpbrd.setContents (new StringSelection (r), null);
    else clpbrd.setContents (new StringSelection (h), null);
    
    if(blue.isVisible())    jPanel1.setVisible(true);
    if(red.isVisible()) jPanel2.setVisible(true);     
    if(purple.isVisible())  jPanel3.setVisible(true); 
    if(yellow.isVisible())  jPanel4.setVisible(true); 
    if(orange.isVisible())  jPanel5.setVisible(true);
    
}

